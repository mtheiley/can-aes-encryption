#include <iostream>
#include "matrix.hpp"
#include "aes128.hpp"

int main() {
    matrix::Matrix key ({
        {0x2b, 0x28, 0xab, 0x09},
        {0x7e, 0xae, 0xf7, 0xcf},
        {0x15, 0xd2, 0x15, 0x4f},
        {0x16, 0xa6, 0x88, 0x3c}
    });

    matrix::Matrix data ({
        {0xde, 0xad, 0xbe, 0xef},
        {0xbe, 0xd0, 0x00, 0x00},
        {0xca, 0xfe, 0xba, 0xbe},
        {0x11, 0x11, 0x11, 0x11}
    });

    AES128 aes128(key);
    aes128.encrypt(data);
    std::cout << std::hex << data << std::endl;
    aes128.decrypt(data);
    std::cout << std::hex << data << std::endl;
}